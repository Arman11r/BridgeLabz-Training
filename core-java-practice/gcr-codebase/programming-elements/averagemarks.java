public class averagemarks {
    public static void main(String[] args) {
        // Marks obtained in 3 subject
        int math = 94;
        int physics = 95;
        int chemistry = 96;
       
        // Calculating total marks here
        int totalMarks = math + physics + chemistry;

        // Calculating average marks here
        double averageMarks = totalMarks / 3.0;

        // Displaying the average marks here
        System.out.println("Sam's Average Marks in PCM is: " + averageMarks);
    }
}
